// Math.round : mmbulatkan sesuai aturan. jika 5+ dibulatkan keatas, jika 4- dibulatkan ke semula
Math.round(5.5);
Math.round(5.4);

// Math.pow : memangkatkan param 2 ke param 1
Math.pow(5, 3);

// Math.sqrt : cari akar kuadrat
Math.sqrt(64);

// Math.abs : jadi nilai mutlak
Math.abs(-5.9);

// Math.ceil : membulatkan keatas tnp peduli aturan
Math.ceil(2.4);

// Math.floor : mmbulatkan kebawah tnp peduli aturan
Math.floor(2.7);


// Math.min : cari nilai terendah
Math.min(30, 93, -100, 2 , 0, 80);
// Math.max : cari nilai terbesar
Math.max(30, 93, -100, 2 , 0, 80);


// Math.random : kasih angka random 0 - 1
Math.random();